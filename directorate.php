                    <h3>Disease Prevention and Control Directorate</h3>
                    <h5>Summary about the directorate</h5>
                    <p class="fst-italic"> •	Disease Prevention and Control Directorate is one of the directorate organized in health bureau. The directorate is responsible to coordinate communicable and non-communicable disease prevention and control, Environmental health and Health education and promotion as well as urban health extension and primary health care reform programs. The Directorate also coordinates the designing and development and intervention packages of strategies, technical guidelines, protocols, Standard Operating Procedures. Moreover, the Directorate is responsible for technical capacity building for health care facilities and professional as well as community level workers delivery of disease prevention and health promotion programs, which is mainly done in collaboration with government and partners. The Directorate also engages in resource mobilization, program monitoring and evaluation, and evidence-based effective interventions selection.</p>
                    <h5>AIM</h5>
                    <p>
•	To end TB by 2030
•	Strengthen HIV/AIDS prevention and mitigation strategies in Addis Ababa 
•	To prevent avoidable non-communicable diseases and mental illness in Addis Ababa
•	To create healthy Environment in collaboration with other sectors 
•	Strengthen health education and promotion at all health provision levels 
</p>
<h5> MISSION</h5>
<p>•	To provide Quality and equitable Health promotion, disease prevention, Curative treatment, and rehabilitation service and exceed the health and safety of our city community to a higher level of standard.<p>
<h5>VISION</h5>
<p> •	To see healthy and productive citizens who can actively keep their wellbeing and to see the city this provides quality of health care service to the community by the end of 2017 E.C</P>
<h5> Directorate roles and responsibilities</h5>
<p>•	Strengthen the prevention and control of communicable, non-communicable diseases and mental health problems.
•	Develop implementation technical manuals for prevention and control of communicable, non-communicable diseases, and mental health and mental disorders.
•	Conduct advocacy on communicable diseases, NCDs, and mental health disorders and their risk factors to increase the awareness and commitment of stakeholders, health managers, and health care workers. 
•	Promote healthy lifestyles, reducing risk factors, and providing integrated and cost-effective public health and clinical interventions.
</p>
